package ftc.vo;

public class MoneyVO {
	private String money_nm  = "";
	private String money_cd = "";
	private String no_money_nm  = "";
	private String no_money_cd = "";

	public String getMoney_nm() {
		return money_nm;
	}
	public void setMoney_nm(String money_nm) {
		this.money_nm = money_nm;
	}
	public String getMoney_cd() {
		return money_cd;
	}
	public void setMoney_cd(String money_cd) {
		this.money_cd = money_cd;
	}
	public String getNo_money_nm() {
		return no_money_nm;
	}
	public void setNo_money_nm(String no_money_nm) {
		this.no_money_nm = no_money_nm;
	}
	public String getNo_money_cd() {
		return no_money_cd;
	}
	public void setNo_money_cd(String no_money_cd) {
		this.no_money_cd = no_money_cd;
	}
}
