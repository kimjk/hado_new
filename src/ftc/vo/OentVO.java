package ftc.vo;

public class OentVO {
	String oent_seq = "";
	String oent_type = "";
	String oent_nm = "";
	String oent_mng = "";
	String dept_seq = "";

	public String getOent_seq() {
		return oent_seq;
	}
	public void setOent_seq(String oent_seq) {
		this.oent_seq = oent_seq;
	}
	public String getOent_type() {
		return oent_type;
	}
	public void setOent_type(String oent_type) {
		this.oent_type = oent_type;
	}
	public String getOent_nm() {
		return oent_nm;
	}
	public void setOent_nm(String oent_nm) {
		this.oent_nm = oent_nm;
	}
	public String getOent_mng() {
		return oent_mng;
	}
	public void setOent_mng(String oent_mng) {
		this.oent_mng = oent_mng;
	}
	public String getDept_seq() {
		return dept_seq;
	}
	public void setDept_seq(String dept_seq) {
		this.dept_seq = dept_seq;
	}
}

 